    private void jButton_db_basicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_db_basicActionPerformed
        if(jTextField_JID.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Input Job Id Values");
        }
        else{
            int JID = Integer.parseInt(jTextField_JID.getText());
            ResultSet rs=null;

            try {

                Statement s = DBconnect.getConnection().createStatement();
                rs= s.executeQuery("SELECT basic_salary, hourly_rate FROM salary WHERE job_id = '"+JID+"'");

                while(rs.next()){
                    int sal  = rs.getInt("basic_salary");
                    jTextField_BS.setText(String.valueOf(sal));

                    int hRate  = rs.getInt("hourly_rate");
                    jTextField_HR.setText(String.valueOf(hRate));
                }

            } catch (Exception e) {
                System.out.println("Exception = " + e.getMessage());

            }
        }
    }//GEN-LAST:event_jButton_db_basicActionPerformed

