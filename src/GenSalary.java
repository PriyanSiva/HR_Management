    private void jButton_generateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_generateActionPerformed
        try{
            bonusRate = Integer.parseInt(jTextField_BR.getText());
            basicSalary = Integer.parseInt(jTextField_BS.getText());
            int hourly_rate = Integer.parseInt(jTextField_HR.getText());
            int overTimeHours = Integer.parseInt(jTextField_OTHours.getText());
            
            totalSalary = basicSalary + (bonusRate*basicSalary)/100 + overTimeHours*hourly_rate;
            
            jTextField_TS.setText(Integer.toString(totalSalary));
            
        }catch (InputMismatchException a){
            JOptionPane.showMessageDialog(null, "Wrong Input Values");
        }
    }//GEN-LAST:event_jButton_generateActionPerformed

